package enumeration;

/**
 * Направление взгляда робота
 * @author Sergii.Tushinskyi
 */
public enum Direction {
    /**
     * ВВЕРХ - по направлению оси Y (+y)
     */
    UP,
    /**
     * ВНИЗ - по направлению оси Y (-y)
     */
    DOWN,
    /**
     * ВЛЕВО - по направлению оси X (-x)
     */
    LEFT,
    /**
     * ВПРАВО - по направлению оси X  (+x)
     */
    RIGHT
}
